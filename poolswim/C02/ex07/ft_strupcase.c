#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	makeup;
	int	i;

	makeup = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] += makeup;
		i++;
	}
	return (str);
}
