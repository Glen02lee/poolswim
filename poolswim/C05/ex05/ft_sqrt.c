#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	sqrt;

	i = 0;
	sqrt = 0;
	if (nb < 1)
		return (0);
	while (1)
	{
		sqrt = i * i;
		if (sqrt == nb)
			return (i);
		else if (sqrt > nb)
			return (0);
		i++;
	}
}
