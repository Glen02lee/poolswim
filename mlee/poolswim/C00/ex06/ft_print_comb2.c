#include <unistd.h>

void	ft_print_comb2(void)
{
	char	line[5];
	int		n1;
	int		n2;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			line[0] = '0' + n1 / 10;
			line[1] = '0' + n1 % 10;
			line[2] = ' ';
			line[3] = '0' + n2 / 10;
			line[4] = '0' + n2 % 10;
			write (1, &line, 5);
			if (n1 != 98)
			{
				write (1, &", ", 2);
			}
			n2++;
		}
		n1++;
	}
}
