#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	arr[20];
	char	num;
	int		i;

	i = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb == 0)
	{
		write(1, "0", 1);
	}
	while (nb != 0)
	{
		num = nb % 10 + '0';
		arr[i++] = num;
		nb /= 10;
	}
	while (i >= 0)
	{
		write(1, &arr[i--], 1);
	}
}
