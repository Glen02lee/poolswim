#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	makelow;
	int	i;

	makelow = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] -= makelow;
		i++;
	}
	return (str);
}
